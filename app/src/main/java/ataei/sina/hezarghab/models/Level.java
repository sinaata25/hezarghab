package ataei.sina.hezarghab.models;

public class Level {
    int id;
    String name;
    String img;
    String color_primary;
    String color_secendary;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getColor_primary() {
        return color_primary;
    }

    public void setColor_primary(String color_primary) {
        this.color_primary = color_primary;
    }

    public String getColor_secendary() {
        return color_secendary;
    }

    public void setColor_secendary(String color_secendary) {
        this.color_secendary = color_secendary;
    }
}
